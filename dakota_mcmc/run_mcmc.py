#!/usr/bin/env python
"""Write Dakota input from external calibration data and run MCMC.

Observation data and cell indices must be prepared separately, e.g. with
``write_observations.py``. This script copies them into ``dakota_mcmc/``,
optionally builds or reuses a proposal covariance, writes ``sod_bayes.in``, and
runs::

    dakota -i <input_file> -o <output_file>

Example workflow::

    python dakota_mcmc/write_observations.py 0.9e5 0.9e4 0.9 0.1 --out-dir dakota_mcmc
    python dakota_mcmc/run_mcmc.py --obs dakota_mcmc/sod_obs.dat \\
        --cells dakota_mcmc/sod_cells.npy --forward euler
    python dakota_mcmc/run_mcmc.py --obs ... --cells ... \\
        --ensemble-dir training_data/shock_tube/enkf_ensemble_files
    python dakota_mcmc/run_mcmc.py --obs ... --cells ... --plot \\
        --plot-truth 0.9e5 0.9e4 0.9 0.1
"""
